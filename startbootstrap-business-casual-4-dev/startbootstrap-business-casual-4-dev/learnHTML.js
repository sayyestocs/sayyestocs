function showPara() {
    var tag_list = document.getElementsByClassName('ptag');
    for (i = 0; i < tag_list.length; i++) {
        var x = tag_list[i];
        if (x.style.display === 'block') {
            x.style.display = 'none';
        } else {
            x.style.display = 'block';
        }
    }
}

